def T(lang: str, key: str, **kw) -> str:
    lang = lang if lang in SUPPORTED else DEFAULT_LANG
    entry = MSG.get(key, {})
    s = entry.get(lang) or entry.get(DEFAULT_LANG) or ""
    return s.format(**kw) if kw else s
