def norm_lang(text: str):
    """把用户输入归一成 'ja' / 'zh'；无法识别返回 None。"""
    t = (text or "").strip().lower()
    if t in ("1", "日本語", "日本语", "日语", "にほんご", "ja", "jp", "japanese"):
        return "ja"
    if t in ("2", "中文", "中国語", "中国语", "汉语", "漢語", "zh", "cn", "chinese"):
        return "zh"
    return None


